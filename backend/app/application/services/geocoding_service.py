DEFAULT_COORDS = (55.7558, 37.6173, 3)  # Moscow


def _normalize(city: str) -> str:
    return re.sub(r"\s+", " ", city.strip().lower())


def resolve_city(city: str | None) -> tuple[float, float, int, str]:
    """Return lat, lon, utc_offset, display label."""
    if not city or not city.strip():
        return *DEFAULT_COORDS, "Москва (по умолчанию)"

    key = _normalize(city)
    if key in CITY_COORDS:
        lat, lon, tz = CITY_COORDS[key]
        return lat, lon, tz, city.strip()

    for name, (lat, lon, tz) in CITY_COORDS.items():
        if name in key or key in name:
            return lat, lon, tz, city.strip()

    return *DEFAULT_COORDS, f"{city.strip()} (координаты Москвы)"
