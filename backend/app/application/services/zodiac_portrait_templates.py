GENDER_NOTE: dict[str, str] = {
    "m": "Ваш мужской опыт рождения добавляет активную, инициативную окраску проявлениям знака.",
    "w": "Ваш женский опыт рождения добавляет интуитивную, принимающую окраску проявлениям знака.",
}


def render_zodiac_portrait(
    name: str,
    zodiac_sign: str,
    gender: str | None = None,
) -> dict[str, str]:
    data = PORTRAITS.get(zodiac_sign, PORTRAITS["Овен"])
    gender_note = GENDER_NOTE.get(gender or "", "")
    sections = [
        ("essence", "Суть знака"),
        ("strengths", "Сильные стороны"),
        ("shadow", "Тень знака"),
        ("love", "Любовь и отношения"),
        ("career", "Работа и реализация"),
        ("advice", "Совет звёзд"),
    ]
    parts = [f"## Портрет {data['emoji']} {zodiac_sign}", f"**{name}**, {data['essence']}"]
    if gender_note:
        parts.append(gender_note)
    result: dict[str, str] = {
        "zodiac_sign": zodiac_sign,
        "emoji": data["emoji"],
        "summary": data["essence"],
    }
    for key, title in sections:
        text = data[key]
        parts.append(f"## {title}\n{text}")
        result[key] = text
    result["text"] = "\n\n".join(parts)
    return result
