def planet_interpretation(planet_name: str, sign: str, house: int | None) -> str:
    by_sign = PLANET_IN_SIGN.get(planet_name, {})
    text = by_sign.get(sign) or by_sign.get("default") or f"{planet_name} в {sign} формирует особый оттенок вашей карты."
    if house:
        area = HOUSE_MEANINGS.get(house, "важную сферу жизни")
        text += f" В {house}-м доме ({area}) эта энергия особенно заметна."
    return text


def chart_summary(name: str, chart: dict) -> str:
    sun = next((p for p in chart["planets"] if p["key"] == "sun"), None)
    moon = next((p for p in chart["planets"] if p["key"] == "moon"), None)
    parts = [f"{name}, ваша натальная карта раскрывает ключевые энергии рождения."]
    if sun:
        parts.append(f"Солнце в {sun['sign']} — ядро личности.")
    if moon:
        parts.append(f"Луна в {moon['sign']} — эмоциональный мир.")
    if chart.get("ascendant"):
        parts.append(f"Асцендент в {chart['ascendant']} — то, как вас видят при первой встрече.")
    elif chart.get("time_unknown"):
        parts.append("Время рождения не указано — асцендент и дома рассчитаны приблизительно (полдень).")
    return " ".join(parts)
