def _advice_for_category(slug: str | None, rnd: random.Random) -> str:
    key = (slug or "general").strip().lower()
    pool = _CATEGORY_ADVICE.get(key, _CATEGORY_ADVICE["general"])
    if rnd.random() < 0.4:
        return rnd.choice(_ADVICE_BANK)
    return rnd.choice(pool)


def _situation_snippet(situation: str, rnd: random.Random) -> str:
    situation = (situation or "").strip()
    if not situation:
        return rnd.choice([h for h in _SITUATION_HOOKS if "{situation}" not in h])
    short = situation if len(situation) <= 120 else situation[:117].rstrip() + "…"
    hook = rnd.choice(_SITUATION_HOOKS)
    if "{situation}" in hook:
        return hook.format(situation=short)
    return hook


def render_fallback(
    name: str,
    category: str,
    situation: str,
    emotion: str,
    cards: list[dict],
    slug: str | None = None,
    seed: int | None = None,
) -> dict[str, str]:
    """Собирает целостный поддерживающий расклад локально, без AI.

    cards: список словарей с ключами position ("Прошлое"/"Настоящее"/"Будущее"),
    name, is_reversed, meaning.
    Возвращает dict с секциями past/present/future/advice/conclusion и полным text.
    """
    rnd = random.Random(seed)
    name = name or "Дорогой друг"
    tone = category_tone(category, slug)

    past = _find(cards, "Прошлое")
    present = _find(cards, "Настоящее")
    future = _find(cards, "Будущее")

    def block(card: dict | None, openers: list[str], reframes: list[str]) -> str:
        if not card:
            return ""
        is_rev = card.get("is_reversed", False)
        opener = rnd.choice(openers).format(
            name=name,
            card=card.get("name", "карта"),
            rev=_rev_suffix(is_rev),
            tone=tone,
        )
        meaning = (card.get("meaning") or "").strip()
        reframe = rnd.choice(reframes)
        parts = [opener]
        if meaning:
            bridge = rnd.choice(_MEANING_BRIDGES).format(name=name, meaning=meaning)
            parts.append(bridge)
        if is_rev:
            parts.append(rnd.choice(_REVERSED_NOTE).strip())
        parts.append(reframe)
        return " ".join(parts)

    intro = _situation_snippet(situation, rnd)
    past_text = block(past, _PAST_OPENERS, _PAST_REFRAMES)
    present_text = block(present, _PRESENT_OPENERS, _PRESENT_REFRAMES)
    if present_text:
        present_text = f"{present_text} {emotion_support(emotion, rnd)}"
    future_text = block(future, _FUTURE_OPENERS, _FUTURE_REFRAMES)

    advice_text = _advice_for_category(slug, rnd)
    conclusion_text = rnd.choice(_CONCLUSION_BANK).format(name=name)

    full = (
        "## Ваш расклад\n\n"
        f"{intro}\n\n"
        f"## Карта прошлого\n{past_text}\n\n"
        f"## Карта настоящего\n{present_text}\n\n"
        f"## Карта будущего\n{future_text}\n\n"
        f"## Совет карт\n{advice_text}\n\n"
        f"## Главный вывод\n{conclusion_text}"
    )

    return {
        "text": full,
        "past": past_text,
        "present": present_text,
        "future": future_text,
        "advice": advice_text,
        "conclusion": conclusion_text,
    }
