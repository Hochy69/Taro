"""Lunar day calculations and template texts."""

from datetime import date

SYNODIC_MONTH = 29.530588853
KNOWN_NEW_MOON_JD = 2451550.1  # ~6 Jan 2000


def _julian_day(d: date) -> float:
    a = (14 - d.month) // 12
    y = d.year + 4800 - a
    m = d.month + 12 * a - 3
    return d.day + (153 * m + 2) // 5 + 365 * y + y // 4 - y // 100 + y // 400 - 32045


def get_lunar_day(birth_date: date) -> int:
    """Approximate lunar day at birth (1–30)."""
    jdn = _julian_day(birth_date)
    lunar_age = (jdn - KNOWN_NEW_MOON_JD) % SYNODIC_MONTH
    day = int(lunar_age) + 1
    return max(1, min(30, day))


