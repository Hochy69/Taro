def render_lunar_birth_text(name: str, birth_date: date) -> dict[str, str]:
    day = get_lunar_day(birth_date)
    title = LUNAR_DAY_NAMES.get(day, f"{day}-й лунный день")
    meaning = LUNAR_DAY_MEANINGS.get(day, "Ваш лунный день несёт особую энергию рождения.")
    return {
        "lunar_day": str(day),
        "title": title,
        "meaning": f"{name}, вы родились в {title}. {meaning}",
        "advice": (
            "Обращайте внимание на лунные фазы в повседневной жизни — "
            "в дни, близкие к вашему лунному дню рождения, интуиция обостряется."
        ),
    }
